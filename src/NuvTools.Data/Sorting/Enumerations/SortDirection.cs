namespace NuvTools.Data.Sorting.Enumerations;

/// <summary>
/// Specifies the direction for sorting operations.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Sort in ascending order (A to Z, 0 to 9, oldest to newest).
    /// </summary>
    ASC = 0,

    /// <summary>
    /// Sort in descending order (Z to A, 9 to 0, newest to oldest).
    /// </summary>
    DESC = 1
}
